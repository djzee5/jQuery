/**
 * jQuery function to hide paragraph elements within
 * the article elements.
 */
function remove() {
    // Hide all paragraph elements within article
    // elements using hide() function of JQuery
    $("article > p").hide();
}

/**
 * jQuery function to show paragraph elements within
 * the article elements.
 */
function display() {
    // Show all paragraph elements within article
    // elements using show() function of JQuery
    $("article > p").show();
}


